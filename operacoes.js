function soma(a, b){
	var soma = a + b;
	return a + " + " + b + " = " + soma;  
};

function subtracao(a, b){
	var subtracao = a - b;
	return a + " + " + b + " = " + subtracao;  
};

function multiplicacao(a, b){
	var multiplicacao = a * b;
	return a + " + " + b + " = " + multiplicacao;  
};

function divisao(a, b){
	var divisao = a / b;
	return a + " + " + b + " = " + divisao;  
};

